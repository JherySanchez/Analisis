    private void configurarTabla() {
        String[] columnas = {"ID Insumo", "Insumo", "Cantidad Necesaria", "Unidad"};
        modeloTabla = new javax.swing.table.DefaultTableModel(null, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblIngredientes.setModel(modeloTabla);
    }

    // Llena los combos desde la base de datos
    private void cargarCombos() {
        controlador.ProductoCtrl prodCtrl = new controlador.ProductoCtrl();
        java.util.List<modelo.entidad.Producto> productos = prodCtrl.listarProductos();
        cbProducto.removeAllItems();
        for (modelo.entidad.Producto p : productos) {
            cbProducto.addItem(p); 
        }

        controlador.InsumoCtrl insumoCtrl = new controlador.InsumoCtrl();
        java.util.List<modelo.entidad.Insumo> insumos = insumoCtrl.listarInsumos();
        cbInsumo.removeAllItems();
        for (modelo.entidad.Insumo i : insumos) {
            cbInsumo.addItem(i);
        }
    }
    
    // Si seleccionas un producto que ya tiene receta, la muestra
    private void cargarRecetaExistente() {
        modelo.entidad.Producto prod = (modelo.entidad.Producto) cbProducto.getSelectedItem();
        if (prod == null) return;
        
        controlador.RecetaCtrl ctrl = new controlador.RecetaCtrl();
        java.util.List<modelo.entidad.Receta> recetaBD = ctrl.obtenerReceta(prod.getId_producto());
        
        listaIngredientesTemp.clear();
        modeloTabla.setRowCount(0);
        
        for (modelo.entidad.Receta r : recetaBD) {
            listaIngredientesTemp.add(r);
            agregarFilaTabla(r);
        }
    }
    
    private void agregarFilaTabla(modelo.entidad.Receta r) {
        modeloTabla.addRow(new Object[]{
            r.getInsumo().getId_insumo(),
            r.getInsumo().getNombre(),
            r.getCantidad_necesaria(),
            r.getInsumo().getUnidad_medida()
        });
    }
    
