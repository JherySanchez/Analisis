    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        modelo.entidad.Insumo insumo = (modelo.entidad.Insumo) cbInsumo.getSelectedItem();
        String cantStr = txtCantidad.getText();

        if (insumo == null) return;

        try {
            double cantidad = Double.parseDouble(cantStr);
            if (cantidad <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cantidad debe ser mayor a 0.");
                return;
            }

            // Evitar duplicados
            for (modelo.entidad.Receta r : listaIngredientesTemp) {
                if (r.getInsumo().getId_insumo() == insumo.getId_insumo()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Ya está en la lista. Bórralo para editar.");
                    return;
                }
            }

            // Crear y agregar
            modelo.entidad.Receta r = new modelo.entidad.Receta();
            r.setInsumo(insumo);
            r.setCantidad_necesaria(cantidad);

            listaIngredientesTemp.add(r);
            agregarFilaTabla(r);

            txtCantidad.setText("");
            cbInsumo.requestFocus();

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un número válido.");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int fila = tblIngredientes.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un ingrediente.");
            return;
        }
        listaIngredientesTemp.remove(fila);
        modeloTabla.removeRow(fila);
    }//GEN-LAST:event_btnQuitarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        modelo.entidad.Producto prod = (modelo.entidad.Producto) cbProducto.getSelectedItem();
    
        if (prod == null || listaIngredientesTemp.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un producto y agregue ingredientes.");
            return;
        }

        int resp = javax.swing.JOptionPane.showConfirmDialog(this, 
                "¿Guardar receta para '" + prod.getNombre() + "'?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);

        if (resp == javax.swing.JOptionPane.YES_OPTION) {
            controlador.RecetaCtrl ctrl = new controlador.RecetaCtrl();
            if (ctrl.guardarReceta(prod.getId_producto(), listaIngredientesTemp)) {
                javax.swing.JOptionPane.showMessageDialog(this, "¡Receta guardada!");
                this.dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar.");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

